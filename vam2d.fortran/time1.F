      SUBROUTINE TIME1 (STIME,ITIME)
C-----CALCULATE TIME IN DDHHMMSS FORMAT
      ISEC=STIME
      IMINIT=ISEC/60
      ISEC=ISEC-IMINIT*60
      IHR=IMINIT/60
      IMINIT=IMINIT-IHR*60
      IDAY=IHR/24
      IHR=IHR-IDAY*24
C-----RESTRICTION FOR SEPT 78 ONLY IS HERE.
      IDAY=IDAY-242
C=====FOR OLD DATA SETS STIME IS SECONDS FROM 0000 GMT 1 SEPT 78
C=====IDAY=IDAY+243
C=====
      ITIME=ISEC+100*IMINIT+10000*IHR+1000000*IDAY
      RETURN
      END
